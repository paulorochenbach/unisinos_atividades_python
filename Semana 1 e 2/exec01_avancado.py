A = float(input("Insira um valor A(Real): "))
B = int(input("Insira um valor B(Inteiro): "))

print("\nA multiplicado por B é {}".format(A*B))
print("A dividido por B é {}".format(A/B))
print("A mais B é {} e A menos B é {}".format(A+B, A-B))
print("A elevado a B é {}".format(A**B))

